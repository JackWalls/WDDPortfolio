function createList(){
    var links = [
        {label: "Week 1", url: "week1/index.html"},
        {label: "Week 2", url: "week2/index.html"}
    ];
    var text = "";
    for (var i = 0; i < 2; i++){
            text += "<li><a href='" + links[i].url + "'>" + links[i].label;
    }
    document.getElementById("list").innerHTML = text;
}



